"""Словарь ручных транскрипций для F5-TTS.

F5-TTS поддерживает управление ударением через символ `+` перед гласной:
"молок+о" → «молокó». RUAccent ставит ударения автоматически, но для
аббревиатур, имён собственных и заимствований часто ошибается.

Этот словарь применяется ДО RUAccent — явно проставленные ударения
RUAccent должен уважать и не перетирать.

Расширяй словарь по мере обнаружения проблем в QA.
"""
from __future__ import annotations

import re

# Ключ — что искать (регистронезависимо), значение — что подставить.
# Не используй `\b` для Cyrillic — re.IGNORECASE + раздельные варианты по падежам.
PRONUNCIATIONS: dict[str, str] = {
    # Аббревиатура КЭСИ — читается как «кэсИ» с ударением на «и»
    "КЭСИ": "кэс+и",
    "Кэси": "кэс+и",
    "кэси": "кэс+и",

    # Склонения
    "колледже КЭСИ": "колл+едже кэс+и",
    "колледжа КЭСИ": "колл+еджа кэс+и",
    "колледж КЭСИ": "колл+едж кэс+и",

    # Добавляй сюда слова которые F5-TTS (или RUAccent поверх него)
    # озвучивает неправильно. Формат: "оригинал": "транскрипция с +".
    # Примеры для расширения:
    # "специальность": "специ+альность",
    # "поступить": "поступ+ить",
    # "Александровна": "Алекс+андровна",
}


# Word boundary: ни перед, ни после не должно быть буквы/цифры (Cyrillic или Latin)
_WORD_BOUNDARY = r"(?<![а-яА-ЯёЁa-zA-Z0-9])({}(?![а-яА-ЯёЁa-zA-Z0-9]))"


def apply_pronunciations(text: str) -> str:
    """Заменяет ключевые слова/фразы на транскрипцию с явными ударениями.

    Замены case-insensitive и с word-boundary для Cyrillic — не затрагивают
    слова где фрагмент встроен внутрь (например, "КЭСИ" заменится в "в КЭСИ",
    но не внутри "КЭСИ2024").

    Длинные ключи ("колледже КЭСИ") применяются до коротких ("КЭСИ"),
    чтобы избежать частичного overwrite.
    """
    # Сортируем по длине убыванием — многословные ключи раньше одиночных.
    for src in sorted(PRONUNCIATIONS, key=len, reverse=True):
        dst = PRONUNCIATIONS[src]
        pattern = _WORD_BOUNDARY.format(re.escape(src))
        text = re.sub(pattern, dst, text, flags=re.IGNORECASE)
    return text
